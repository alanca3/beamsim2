"""V-2: pulsating/oscillating sphere benchmark against exact closed-form solutions, mirroring NumCalc's own validation suite."""

from __future__ import annotations

import math

import numpy as np

from beamsim2.core.types import BoundaryConditions, Mesh, ObservationPoints

# ---------------------------------------------------------------------------
# Mesh builder (migrated from tests/test_numcalc_roundtrip.py)
# ---------------------------------------------------------------------------


def make_pulsating_sphere_mesh(radius: float, subdivisions: int) -> tuple[Mesh, BoundaryConditions]:
    """Build an icosphere BEM mesh for the pulsating-sphere benchmark.

    The sphere is centred at the global coordinate origin with all surface
    elements in group 1 and unit normal velocity (1+0j). The icosphere is
    generated by midpoint subdivision of a regular icosahedron; vertex
    positions are re-normalised to the sphere surface after each subdivision
    so the mesh converges to a true sphere.

    In acoustics terms: this is the geometry of an omnidirectional (monopole)
    source — a rigid sphere whose entire surface vibrates radially with uniform
    normal velocity.

    Triangle winding is verified outward: the outward normal (cross product
    of two edges) must point away from the origin. Inward-facing triangles
    have their winding flipped.

    Parameters
    ----------
    radius : float
        Sphere radius in metres.
    subdivisions : int
        Midpoint-subdivision iterations.
        0 → 20 triangles (icosahedron), 1 → 80, 2 → 320, 3 → 1280.

    Returns
    -------
    mesh : Mesh
        vertices [V, 3] float64, triangles [T, 3] int32,
        group_tags [T] int32 = 1 (all vibrating).
    bc : BoundaryConditions
        group 1 vibrating at VELO = 1+0j (unit cone velocity).
    """
    phi = (1.0 + math.sqrt(5.0)) / 2.0  # golden ratio ≈ 1.618

    # 12 icosahedron vertices, normalised to unit sphere
    raw_verts = np.array(
        [
            [-1, phi, 0],
            [1, phi, 0],
            [-1, -phi, 0],
            [1, -phi, 0],
            [0, -1, phi],
            [0, 1, phi],
            [0, -1, -phi],
            [0, 1, -phi],
            [phi, 0, -1],
            [phi, 0, 1],
            [-phi, 0, -1],
            [-phi, 0, 1],
        ],
        dtype=np.float64,
    )
    raw_verts /= np.linalg.norm(raw_verts, axis=1, keepdims=True)

    # 20 faces, CCW winding viewed from outside
    raw_faces = np.array(
        [
            [0, 11, 5],
            [0, 5, 1],
            [0, 1, 7],
            [0, 7, 10],
            [0, 10, 11],
            [1, 5, 9],
            [5, 11, 4],
            [11, 10, 2],
            [10, 7, 6],
            [7, 1, 8],
            [3, 9, 4],
            [3, 4, 2],
            [3, 2, 6],
            [3, 6, 8],
            [3, 8, 9],
            [4, 9, 5],
            [2, 4, 11],
            [6, 2, 10],
            [8, 6, 7],
            [9, 8, 1],
        ],
        dtype=np.int32,
    )

    verts = raw_verts
    faces = raw_faces

    for _ in range(subdivisions):
        verts, faces = _subdivide(verts, faces)

    verts = verts * radius

    # Verify outward normals: centroid · normal > 0 means outward
    v0 = verts[faces[:, 0]]  # [T, 3]
    v1 = verts[faces[:, 1]]  # [T, 3]
    v2 = verts[faces[:, 2]]  # [T, 3]
    normals = np.cross(v1 - v0, v2 - v0)  # [T, 3]
    centroids = (v0 + v1 + v2) / 3.0  # [T, 3]
    dots = np.einsum("ij,ij->i", normals, centroids)  # [T]
    inward = dots < 0
    if inward.any():
        faces[inward] = faces[inward][:, [0, 2, 1]]  # flip winding

    group_tags = np.ones(len(faces), dtype=np.int32)  # [T] — all group 1
    mesh = Mesh(
        vertices=verts.astype(np.float64),
        triangles=faces.astype(np.int32),
        group_tags=group_tags,
    )
    bc = BoundaryConditions(vibrating_groups={1: complex(1.0, 0.0)})
    return mesh, bc


def _subdivide(verts: np.ndarray, faces: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    """One iteration of midpoint subdivision for a sphere mesh.

    Splits each triangle into four by inserting edge midpoints, then
    projects new vertices back onto the unit sphere.

    Parameters
    ----------
    verts : np.ndarray, shape [V, 3], float64
        Unit-sphere vertex positions.
    faces : np.ndarray, shape [T, 3], int32
        Triangle vertex indices.

    Returns
    -------
    tuple[np.ndarray, np.ndarray]
        (new_verts [V', 3] float64, new_faces [4T, 3] int32).
    """
    edge_midpoints: dict[tuple[int, int], int] = {}
    new_verts = list(verts)

    def midpoint(a: int, b: int) -> int:
        key = (min(a, b), max(a, b))
        if key not in edge_midpoints:
            mid = (new_verts[a] + new_verts[b]) / 2.0
            mid /= np.linalg.norm(mid)  # project to unit sphere
            edge_midpoints[key] = len(new_verts)
            new_verts.append(mid)
        return edge_midpoints[key]

    new_faces: list[list[int]] = []
    for a, b, c in faces:
        ab = midpoint(a, b)
        bc = midpoint(b, c)
        ca = midpoint(c, a)
        new_faces.extend([[a, ab, ca], [b, bc, ab], [c, ca, bc], [ab, bc, ca]])

    return np.array(new_verts, dtype=np.float64), np.array(new_faces, dtype=np.int32)


# ---------------------------------------------------------------------------
# Analytic pulsating-sphere pressure
# ---------------------------------------------------------------------------


def pulsating_sphere_pressure(a: float, k: float, r_obs: float, rho: float, c: float) -> complex:
    """Exact pressure at distance r_obs from a pulsating sphere of radius a.

    For a sphere of radius a vibrating with uniform radial surface velocity
    U₀ = 1 m/s (unit velocity), the complex pressure in the far field is:

        p(r) = ρc · (jka / (1 + jka)) · (a/r) · exp(−jk(r − a))

    VERIFIED: Kinsler, Frey, Coppens, Sanders,
    *Fundamentals of Acoustics*, 4th ed., §7.4 (2000).

    The field is omnidirectional (depends only on r, not on direction),
    so this function returns a single complex number for any observation
    direction at the given r_obs.

    Parameters
    ----------
    a : float
        Sphere radius in metres.
    k : float
        Wave number 2π f / c in rad/m.
    r_obs : float
        Observation distance in metres (> a).
    rho : float
        Air density in kg/m³.
    c : float
        Speed of sound in m/s.

    Returns
    -------
    complex
        Complex pressure in Pa at distance r_obs for unit surface velocity.
    """
    ka = k * a
    jka = 1j * ka
    radiation_factor = jka / (1.0 + jka)  # → 0 as ka→0; → 1 as ka→∞
    return rho * c * radiation_factor * (a / r_obs) * np.exp(-1j * k * (r_obs - a))


# ---------------------------------------------------------------------------
# Error computation for V-2
# ---------------------------------------------------------------------------


def sphere_benchmark_errors(
    H_bem: np.ndarray,
    frequencies: np.ndarray,
    obs_points: ObservationPoints,
    a: float,
    c: float,
    rho: float,
) -> dict:
    """Compare BEM pressure magnitudes to the pulsating-sphere analytic solution.

    For each frequency, the analytic pressure is the same at every direction
    (isotropic monopole). The magnitude error at each observation point and
    frequency is computed in dB. The pass criterion is V-2: mean |error| ≤ 0.5 dB
    at every frequency.

    Parameters
    ----------
    H_bem : np.ndarray, shape [F, N], complex128
        BEM complex pressure at the observation points.
    frequencies : np.ndarray, shape [F], float64
        Frequencies in Hz.
    obs_points : ObservationPoints
        Observation sphere (used for radius r_obs).
    a : float
        Sphere radius in metres.
    c : float
        Speed of sound in m/s.
    rho : float
        Air density in kg/m³.

    Returns
    -------
    dict with keys:
        mag_error_db : np.ndarray, shape [F, N], float64
            Signed dB error at each frequency and direction.
        mean_mag_db : np.ndarray, shape [F], float64
            Mean of |mag_error_db| per frequency.
        max_mag_db : np.ndarray, shape [F], float64
            Max of |mag_error_db| per frequency.
        passed : bool
            True if all mean_mag_db ≤ 0.5 dB.
    """
    r_obs = obs_points.radius
    n_freq, n_obs = H_bem.shape

    # Analytic pressure: isotropic, same value at all N directions for each freq
    p_analytic = np.array(
        [pulsating_sphere_pressure(a, 2.0 * np.pi * f / c, r_obs, rho, c) for f in frequencies],
        dtype=np.complex128,
    )  # [F]

    # Magnitude error in dB: positive if BEM is louder than analytic
    mag_bem = np.abs(H_bem)  # [F, N]
    mag_analytic = np.abs(p_analytic)[:, np.newaxis]  # [F, 1] — broadcast to [F, N]
    mag_error_db = 20.0 * np.log10(mag_bem / mag_analytic)  # [F, N]

    mean_mag_db = np.mean(np.abs(mag_error_db), axis=1)  # [F]
    max_mag_db = np.max(np.abs(mag_error_db), axis=1)  # [F]
    passed = bool(np.all(mean_mag_db <= 0.5))

    return {
        "mag_error_db": mag_error_db,
        "mean_mag_db": mean_mag_db,
        "max_mag_db": max_mag_db,
        "passed": passed,
    }
